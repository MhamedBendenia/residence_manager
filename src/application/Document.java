    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
    if(JOptionPane.showConfirmDialog(null,"Etes vous sûr de vouloir supprimer une chambre ?",
                "Supprimer chambre",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){ 
        try{  
            if(numerochambretxt.getText().length() !=0)
                statement.executeUpdate("Delete from Document1 where numchambre ="+numerochambretxt.getText());
            else 
                JOptionPane.showMessageDialog(null, "Remplire le champ Numero SVP !");
            }    
     
      
      catch(Exception e){
                JOptionPane.showInternalMessageDialog(null,"Erreur de suppréssion "+e.getMessage());
            }
    }
      
        // Pour vider les champs
        numerochambretxt.setText(null);   
        numerobloctxt.setText(null);
        autretxt.setText(null);
        actualiser();
    }//GEN-LAST:event_supprimerActionPerformed


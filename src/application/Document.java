 private void deplace(int i){
        try{
            numerochambretxt.setText(model.getValueAt(i,0).toString());
            numerobloctxt.setText(model.getValueAt(i,1).toString());
            autretxt.setText(model.getValueAt(i,2).toString()); 
        }catch(Exception e){
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "erreur de déplacement "+e.getLocalizedMessage());
        }
    }
   public void actualiser(){
        try{
            model.setRowCount(0);
            statement=maConnexion.obtenirconnexion().createStatement();
            result= statement.executeQuery("select * from document1");
             while(result.next()){
                model.addRow(new Object[]{result.getString("numchambre"),result.getString("numbloc"),result.getString("autre")});
        }}catch(Exception e){
            System.err.println(e);
            table.setModel(model);
        }
        
    }

    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed

        String numerochambre=numerochambretxt.getText().toString();
        String numbloc=numerobloctxt.getText().toString();
        String autre=autretxt.getText().toString();
        String requete="insert into document1(numchambre,numbloc,autre)VALUES('"+numerochambre+"','"+numbloc+"','"+autre+"')";

        try{
            if(numerochambre.equals("")||numbloc.equals("")){
               
            JOptionPane.showMessageDialog(null,"Veuillez vérifier vos données svp !");
            if(numerochambre.equals("")){
                numerochambretxt.setBackground(Color.red);
            }
            if(numbloc.equals(""))   
                 numerobloctxt.setBackground(Color.red);
            }else{
            statement.executeUpdate(requete);
            actualiser();
            Sauvegarde sauvegarde =new Sauvegarde();
            sauvegarde.setVisible(true);
   
                
            // Pour vider les champs
        numerochambretxt.setText(null);   
        numerobloctxt.setText(null);
        autretxt.setText(null);
        numerochambretxt.setBackground(Color.white);
        numerobloctxt.setBackground(Color.white);

            actualiser();
            }
        }  catch(Exception ex){
        
            JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
        }

        

    }//GEN-LAST:event_ajouterActionPerformed

